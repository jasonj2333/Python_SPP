a = 5
b = 7

#Operator równości ==
if a == b:
    print("a i b są sobie równe")
else:
    print("a i b są różne")
    
#Operator różny !=
if a != b:
    print("a i b są różne")
else:
    print("a i b nie są różne")
    
#Operator większości >
if a > b:
    print("a jest większe od b")
else:
    print("a nie jest większe b")
    
#Operator mniejszości <
if a < b:
    print("a jest mniejsze od b")
else:
    print("a nie jest mniejsze b")
    
#Operator większe lub równe >=
if a >= b:
    print("a jest większe lub równe b")
else:
    print("a jest mniejsze b")
    
#Operator mniejsze lub równe <=
if a <= b:
    print("a jest mniejsze lub równe b")
else:
    print("a jest większe b")