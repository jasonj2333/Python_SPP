a = int( input("Podaj 1 liczbę: ") )
b = int( input("Podaj 2 liczbę: ") )

suma = a + b
print("Suma wynosi: ", suma)

roznica = a - b
print(f"Różnica wynosi: {roznica}")

iloczyn = a * b
iloraz = a / b
iloraz_calk = a // b
print(f"Iloczyn wynosi: {iloczyn}")
print(f"Iloraz wynosi: {iloraz}")
print(f"Iloraz całkowity: {iloraz_calk}")
reszta = a % b
print(f"Reszta z dzielania: {reszta}")
obwod = 2 * a + 2 * b
print(f"Obwód prostokąta o bokach {a} i {b} wynosi {obwod}")
potegowanie = a**b
print(f"{a} do potęgi {b} = {potegowanie}")
