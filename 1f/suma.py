a = int( input("Podaj 1 liczbę: ") )
b = int( input("Podaj 2 liczbę: ") )

suma = a + b
print(f"Suma wynosi: {suma}")

roznica = a - b
print(f"Różnica wynosi: {roznica}")

iloczyn = a * b
print(f"Iloczyn wynosi: {iloczyn}")

iloraz = a / b
print(f"Iloraz wynosi: {iloraz}")

iloraz_calkowity = a // b
print(f"Iloraz całkowity wynosi: {iloraz_calkowity}")

reszta_z_dzielenia = a % b
print(f"Reszta z dzielenia wynosi: {reszta_z_dzielenia}")

potegowanie = a ** 3
print(f"{a} do potęgi 3 wynosi: {potegowanie}")

pierwiastek = a ** (1/3)
print(f"Piewiastek 3 stopnia {a} wynosi: {pierwiastek}")